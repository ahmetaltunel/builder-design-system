import SwiftUI
import BuilderFoundation

/// A native macOS visual effect view that bridges to SwiftUI.
/// This is used by the design system surfaces (like Sidebar and Titlebar)
/// when their material definition specifies that they are translucent.
struct SystemVisualEffectView: NSViewRepresentable {
    var material: NSVisualEffectView.Material
    var blendingMode: NSVisualEffectView.BlendingMode

    func makeNSView(context: Context) -> NSVisualEffectView {
        let view = NSVisualEffectView()
        view.material = material
        view.blendingMode = blendingMode
        view.state = .active
        return view
    }

    func updateNSView(_ nsView: NSVisualEffectView, context: Context) {
        nsView.material = material
        nsView.blendingMode = blendingMode
    }
}
